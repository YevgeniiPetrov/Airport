drop database if exists airport;

create database airport;

use airport;

drop table if exists passenger;

create table passenger (
	id int auto_increment not null,
	first_name varchar(32) not null,
	last_name varchar(32) not null,
	birth_date date not null,
	passport varchar(16) not null,
	removed boolean,
	primary key(id)
);

drop table if exists ticket;

create table ticket (
	id int auto_increment not null,
    creation_date datetime not null,
	number int not null,
	place int not null,
	passenger_id int not null,
	removed boolean,
	primary key(id),
	foreign key(passenger_id) references passenger(id)
		on update cascade
		on delete restrict
);

drop table if exists flight;

create table flight (
	id int auto_increment not null,
	departure datetime not null,
	arrival datetime not null,
	removed boolean,
	primary key(id)
);

alter table ticket
add flight_id int not null;

alter table ticket
add constraint fk_ticket_flight
	foreign key(flight_id) references flight(id)
		on update cascade
		on delete restrict;

drop table if exists route;

create table route (
	id int auto_increment not null,
	from_point varchar(32) not null,
	to_point varchar(32) not null,
	removed boolean,
	primary key(id)
);

alter table flight
add route_id int not null;

alter table flight
add constraint fk_flight_route
	foreign key(route_id) references route(id)
		on update cascade
		on delete restrict;

drop table if exists airline;

create table airline (
	id int auto_increment not null,
	title varchar(32) not null,
	rating decimal(3, 2) not null,
	removed boolean,
	primary key(id)
);

drop table if exists flight_airline;

create table flight_airline (
	flight_id int not null,
	airline_id int not null,
	primary key(flight_id, airline_id),
	foreign key(flight_id) references flight(id)
		on update cascade
		on delete cascade,
	foreign key(airline_id) references airline(id)
		on update cascade
		on delete cascade
);

